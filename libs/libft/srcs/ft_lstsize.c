#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;
	t_list *str;

	i = 0;
	str = NULL;
	str = lst;
	while (str)
	{
		i++;
		(str) = (str)->next;
	}
	return (i);
}
