#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char		*s1;
	const char	*s2;

	s1 = dst;
	s2 = src;
	if (dst == src)
		return (0);
	while (n--)
		*s1++ = *s2++;
	return (dst);
}
